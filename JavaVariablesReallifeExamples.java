public class JavaVariablesReallifeExamples {
    public static void main(String[] args) {
        // Student data
        String studentName = "Elliot Rhymes"; // Name of the student
        int studentID = 4321; // Unique ID for the student
        int studentAge = 16; // Age of the student
        float studentFee = 75.35f;
        char studentGrade = 'B'; // Grade of the student

        // Print Variables
        System.out.println("Student Name: " + studentName);
        System.out.println("Student ID: " + studentID);
        System.out.println("Student age: " + studentAge);
        System.out.println("Student Fee: " + studentFee);
        System.out.println("Student grade: " + studentGrade);
    }
}