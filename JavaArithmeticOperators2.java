public class JavaArithmeticOperators2 {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;
        System.out.println(a / b);  // Integer division, result is 3

        double c = 10.0d;
        double d = 3.0d;
        System.out.println(c / d);  // Decimal division, result is 3.333...
    }
}